class FSBOffice(models.Model):
    # Основная информация
    name = models.CharField(
        max_length=255, 
        verbose_name="Наименование органа",
        unique=True
    )
    address = models.TextField(verbose_name="Адрес")
    
    # Регион (выбор из фиксированного списка)
    region = models.CharField(
        max_length=255, 
        verbose_name="Регион",
        choices=RUSSIAN_REGIONS,
        db_index=True
    )
    
    # Контактная информация
    phone_operator = models.CharField(
        max_length=20,
        verbose_name="Телефон оперативного дежурного",
        validators=[
            RegexValidator(
                regex=r'^\+?1?\d{9,15}$',
                message="Телефон должен быть в формате: '+79999999999'"
            )
        ]
    )
    
    phone_communication = models.CharField(
        max_length=20,
        verbose_name="Телефон дежурного по связи",
        validators=[
            RegexValidator(
                regex=r'^\+?1?\d{9,15}$',
                message="Телефон должен быть в формате: '+79999999999'"
            )
        ]
    )
    
    fax = models.CharField(
        max_length=20,
        verbose_name="Факс",
        blank=True,
        null=True,
        validators=[
            RegexValidator(
                regex=r'^\+?1?\d{9,15}$',
                message="Факс должен быть в формате: '+79999999999'"
            )
        ]
    )
    
    email = models.EmailField(
        verbose_name="Адрес электронной почты",
        blank=True,
        null=True
    )
    
    # Дополнительные поля
    description = models.TextField(
        verbose_name="Дополнительная информация",
        blank=True,
        null=True
    )
    
    is_active = models.BooleanField(
        default=True,
        verbose_name="Активный"
    )
    
    created_at = models.DateTimeField(
        auto_now_add=True,
        verbose_name="Дата создания"
    )
    
    updated_at = models.DateTimeField(
        auto_now=True,
        verbose_name="Дата обновления"
    )
    
    class Meta:
        verbose_name = "Территориальный орган ФСБ"
        verbose_name_plural = "Территориальные органы ФСБ"
        ordering = ['region', 'name']
        indexes = [
            models.Index(fields=['region']),
            models.Index(fields=['is_active']),
        ]
    
    def __str__(self):
        return f"{self.name} ({self.region})"